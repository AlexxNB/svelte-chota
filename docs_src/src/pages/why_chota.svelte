<h1>Why chota?</h1>

<p>When you decides to use Svelte in your projects, you expects very tiny bundles of result code.</p>
<p>So the <a href="https://jenil.github.io/chota/" target="_blank">chota</a> is a super light-weight CSS framework, which will add only ~3kb of gzipped code to your bundle.</p>
<p>Svelte-chota is UI kit for easy using chota in your svelte projects.</p>
